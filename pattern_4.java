public class pattern_4 {
    public static void main(String[] args) {
        int n = 4;
        for(int i = 1; i <= n ; i++){
            for( int j = 1; j <= n + 1 ; j++){
                System.out.print(j+" "); // i+ if u want 11111 ,22222
            }
            System.out.print(" \n");
        }
    }
    
}

